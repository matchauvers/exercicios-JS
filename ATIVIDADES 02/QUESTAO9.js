var baseMaior = Number(prompt("Informe a base maior do trapézio: "));
var baseMenor = Number(prompt("Informe a base menor do trapézio: "));
var altura = Number(prompt("Informe a altura do trapézio: "));
var area = ((baseMaior + baseMenor) * altura / 2);
alert("A área do trapézio informado é: " + area);
